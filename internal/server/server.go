package server

import (
	"fmt"
	"testovoe/config"
	"testovoe/internal/auth"
	auth_repository "testovoe/internal/auth/repository"
	auth_usercase "testovoe/internal/auth/usecase"
	"testovoe/pkg/logger"
	"testovoe/pkg/pg"

	"github.com/gofiber/fiber/v3"
)

type server struct {
	app    *fiber.App
	logger *logger.Logger
	cfg    *config.Config
}

func NewServer(logger *logger.Logger, cfg *config.Config) *server {
	return &server{
		app:    fiber.New(),
		logger: logger,
		cfg:    cfg,
	}
}

func (s *server) MapRoutes(pgConn *pg.Pg) {
	authRepo := auth_repository.NewRepository(pgConn)
	authUc := auth_usercase.NewUsecase(authRepo, s.cfg, s.logger)
	authHandlers := auth.NewHandlers(authUc, s.logger)
	authHandlers.MapRoutes(s.app.Group("/user"))
}

